public class Nilai {
      double nk, nt, nh, nuas, nuts, rata; 
 
    double nilaiRata() { 
        rata = (0.1 * nk) + (0.15 * nt) + (0.25 * nuts) + (0.15 
* nuas); 
        return rata; 
    }
    
}
